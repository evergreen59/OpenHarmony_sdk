int32_t LoadCommonCert(mbedtls_x509_crt* chain)
{
    if (chain == NULL) {
        ATTEST_LOG_ERROR("[LoadCommonCert] Invalid parameter");
        return ERR_NET_PARSE_CERT_FAIL;
    }
    int32_t ret = mbedtls_x509_crt_parse_der(chain, g_commonCerts.certDer, g_commonCerts.certLen);
    if (ret < 0) {
        ATTEST_LOG_ERROR("[LoadCommonCert] Parse x509 certificate failed, ret = -0x%x", -ret);
        return ERR_NET_PARSE_CERT_FAIL;
    }
    return ATTEST_OK;
}

int32_t LoadCommonOtherCert(mbedtls_x509_crt* chain, uint32_t cursor)
{
    if (chain == NULL || cursor >= ARRAY_SIZE(g_commonOtherCert)) {
        ATTEST_LOG_ERROR("[LoadCommonOtherCert] Invalid parameter");
        return ERR_NET_PARSE_CERT_FAIL;
    }
    int32_t ret = mbedtls_x509_crt_parse_der(chain, g_commonOtherCert[cursor].certDer,
                                             g_commonOtherCert[cursor].certLen);
    if (ret < 0) {
        ATTEST_LOG_ERROR("[LoadCommonOtherCert] Parse x509 certificate failed, ret = -0x%x", -ret);
        return ERR_NET_PARSE_CERT_FAIL;
    }
    return ATTEST_OK;
}

